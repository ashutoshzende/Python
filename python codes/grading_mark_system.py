marks=int(input("Enter Your Marks:\n"))

if(marks>=90):
    print("Your grade is EXELENT")
elif(marks>=80):
    print("Your grade is A")
elif(marks>=70):
    print("Your grade is B")
elif(marks>=60):
    print("Your grade is C")
elif(marks>=50):
    print("Your grade is D")
elif(marks>=40):
    print("Your grade is F")
else:
    print("It is invalid")